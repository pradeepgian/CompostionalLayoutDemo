import UIKit

extension UIColor {
    
    static var customGreenColor: UIColor {
        return UIColor(red: 95/255, green: 179/255, blue: 161/255, alpha: 1)
    }
    
    static var customBlackColor: UIColor {
        return UIColor(red: 13/255, green: 16/255, blue: 49/255, alpha: 1)
    }
    
    static var customGrayColor: UIColor {
        return UIColor(red: 95/255, green: 96/255, blue: 133/255, alpha: 1)
    }
    
    static var customLightBackgroundColor: UIColor {
        return UIColor(red: 242/255, green: 242/255, blue: 247/255, alpha: 1)
    }
    
}
